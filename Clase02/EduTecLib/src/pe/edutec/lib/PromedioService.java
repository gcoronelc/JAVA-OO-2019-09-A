public class PromedioService {

	public int promedio(int n1, int n2){
		// Variables
		int pr;
		// Proceso
		pr = (n1 + n2) / 2;
		// Reporte
		return pr;
	}
	
	public int promedio(int n1, int n2, int n3){
		// Variables
		int pr;
		// Proceso
		pr = (n1 + n2 + n3) / 3;
		// Reporte
		return pr;
	}
	
	public int promedio(int n1, int n2, int n3, int n4){
		// Variables
		int pr;
		// Proceso
		pr = (n1 + n2 + n3 + n4) / 4;
		// Reporte
		return pr;
	}
	
	public int promedio(int n1, int n2, int n3, int n4, int n5){
		// Variables
		int pr;
		// Proceso
		pr = (n1 + n2 + n3 + n4 + n5) / 5;
		// Reporte
		return pr;
	}
	
}
